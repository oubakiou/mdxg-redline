// Step 3b: KaTeX CSS のフォント data URI 化 + minimal/extra への分離。
// `katex/dist/katex.min.css` を読み、`@font-face { ... url(fonts/*.woff2) ... }` ブロックを
// `url(data:font/woff2;base64,...)` に書き換え、`url(*.ttf)` / `url(*.woff)` は
// `url(about:blank)` に潰す。woff2 fallthrough は modern target 全サポート前提
// (docs/mdxg-math-rendering.archive.md §3.1 / §5.g)。
//
// 2 系統に分岐して書き出す:
//   - dist/katex/katex.css         : minimal セット (Main / AMS / Math / Size1〜4 の 9 family)
//                                     + 全 `.katex` CSS ルール (font-face 以外を全部)
//   - dist/katex/katex-fonts-extra.css : extra セット (Caligraphic / Fraktur / Script /
//                                       SansSerif / Typewriter の 11 family) の `@font-face` のみ
// CLI の `--math-fonts all` 指定時 / standalone build は両方を inline する
// (Step 3c / Step 4)。
//
// KaTeX major version up 時にこの分類が破れていないか先頭の version assert が検知する。
// scripts/lib/shiki-meta.mjs と同じ「version pin + dist 構造 assert」パターン
// (docs/mdxg-math-rendering.archive.md §5.j)。

import { dirname, resolve } from 'node:path'
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'

const ROOT_DIR = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const KATEX_DIST = resolve(ROOT_DIR, 'node_modules', 'katex', 'dist')
const FONTS_DIR = resolve(KATEX_DIST, 'fonts')
const OUT_DIR = resolve(ROOT_DIR, 'dist', 'katex')

// Step 1 PoC で挙動を確定した KaTeX バージョン。major / minor / patch まで pin することで、
// 仕様変更 (dist 構造 / fontset 構成 / `\href` の trust: false 挙動など) が起きた瞬間に
// build を fail させて再評価を強制する (docs/mdxg-math-rendering.archive.md §5.j)。
const EXPECTED_KATEX_VERSION = '0.16.47'

// minimal セットに含めるフォント family 名 (KaTeX_ prefix 後の root 名)。
// 数式の支配的記法 (\frac / \sum / \int / ギリシャ文字 / 行列 / 角括弧 / \mathbb) を網羅する。
const MINIMAL_FAMILY_ROOTS = new Set(['AMS', 'Main', 'Math', 'Size1', 'Size2', 'Size3', 'Size4'])

const HEADER_COMMENT_MINIMAL = `/* Generated by scripts/build-katex-css.mjs from katex@${EXPECTED_KATEX_VERSION}.
 * minimal family set (Main / AMS / Math / Size1-4): docs/mdxg-math-rendering.archive.md §5.g / §5.l
 * Do not edit by hand — re-run \`npm run build:katex\` to regenerate. */
`

const HEADER_COMMENT_EXTRA = `/* Generated by scripts/build-katex-css.mjs from katex@${EXPECTED_KATEX_VERSION}.
 * extra family set (Caligraphic / Fraktur / Script / SansSerif / Typewriter):
 * see docs/mdxg-math-rendering.archive.md §5.g / §5.l. CLI \`--math-fonts all\` / standalone build only.
 * Do not edit by hand — re-run \`npm run build:katex\` to regenerate. */
`

const assertKatexVersion = async () => {
  const pkgPath = resolve(KATEX_DIST, '..', 'package.json')
  const raw = await readFile(pkgPath, 'utf8')
  const parsed = JSON.parse(raw)
  if (parsed.version !== EXPECTED_KATEX_VERSION) {
    throw new Error(
      `katex version mismatch: expected ${EXPECTED_KATEX_VERSION}, got ${parsed.version}. ` +
        'docs/mdxg-math-rendering.archive.md §5.j に従い設計判断 (§5.f / §5.g / Step 5b) を再評価してください。'
    )
  }
}

// `@font-face{font-display:block;font-family:"KaTeX_SansSerif";...}` 形式から root 名を取り出す。
// 引用符 (`"KaTeX_SansSerif"`) と裸 (`KaTeX_Main`) の両方を許容する。
const extractFontFamilyRoot = (block) => {
  const match = /font-family:"?KaTeX_([A-Za-z0-9]+)"?/.exec(block)
  if (match === null) {
    return null
  }
  const [, root] = match
  return root
}

// `url(fonts/X.woff2)` の 1 件を data URI に置換する非同期ステップ。
// woff2 以外は about:blank に潰す (woff2 への fallthrough 前提)。
const buildUrlReplacement = async (match) => {
  const [original, file] = match
  if (file.endsWith('.woff2')) {
    const buf = await readFile(resolve(FONTS_DIR, file))
    return {
      from: original,
      kind: 'inlined',
      to: `url(data:font/woff2;base64,${buf.toString('base64')})`,
    }
  }
  return { from: original, kind: 'dropped', to: 'url(about:blank)' }
}

const inlineWoff2InBlock = async (block, stats) => {
  const matches = [...block.matchAll(/url\(fonts\/([^)]+)\)/g)]
  const replacements = await Promise.all(matches.map(buildUrlReplacement))
  let result = block
  for (const { from, kind, to } of replacements) {
    result = result.replace(from, to)
    if (kind === 'inlined') {
      stats.inlined += 1
    } else {
      stats.dropped += 1
    }
  }
  return result
}

const splitFontFaces = (css) => {
  const blocks = []
  let lastEnd = 0
  for (const match of css.matchAll(/@font-face\{[^}]*\}/g)) {
    if (match.index > lastEnd) {
      blocks.push({ content: css.slice(lastEnd, match.index), kind: 'other' })
    }
    blocks.push({ content: match[0], kind: 'fontface' })
    lastEnd = match.index + match[0].length
  }
  if (lastEnd < css.length) {
    blocks.push({ content: css.slice(lastEnd), kind: 'other' })
  }
  return blocks
}

// font-face ブロックを minimal/extra のどちらに割り振るか + woff2 inline 後の content。
const classifyFontFaceBlock = async (block, stats) => {
  const root = extractFontFamilyRoot(block.content)
  const rewritten = await inlineWoff2InBlock(block.content, stats)
  const isMinimal = root !== null && MINIMAL_FAMILY_ROOTS.has(root)
  if (isMinimal) {
    stats.minimal += 1
    return { content: rewritten, target: 'minimal' }
  }
  stats.extra += 1
  return { content: rewritten, target: 'extra' }
}

const collectFontFaceClassifications = async (blocks, stats) =>
  Promise.all(
    blocks
      .filter((block) => block.kind === 'fontface')
      .map(async (block) => classifyFontFaceBlock(block, stats))
  )

const appendBlock = (block, ctx) => {
  if (block.kind === 'other') {
    ctx.buckets.minimal.push(block.content)
    return
  }
  const classification = ctx.classifications[ctx.cursor.value]
  ctx.cursor.value += 1
  ctx.buckets[classification.target].push(classification.content)
}

const distributeBlocks = async (blocks, stats) => {
  const classifications = await collectFontFaceClassifications(blocks, stats)
  const ctx = {
    buckets: { extra: [HEADER_COMMENT_EXTRA], minimal: [HEADER_COMMENT_MINIMAL] },
    classifications,
    cursor: { value: 0 },
  }
  for (const block of blocks) {
    appendBlock(block, ctx)
  }
  return { extraCss: ctx.buckets.extra.join(''), minimalCss: ctx.buckets.minimal.join('') }
}

const reportStats = (stats, minimalCss, extraCss) => {
  // ビルド統計を stdout に出すのが目的の reporter のため no-console を無効化する。
  /* eslint-disable no-console */
  console.log(
    `[build-katex-css] minimal=${stats.minimal} families, extra=${stats.extra} families, ` +
      `${stats.inlined} woff2 inlined, ${stats.dropped} ttf/woff dropped`
  )
  console.log(
    `[build-katex-css] dist/katex/katex.css ${minimalCss.length} bytes, ` +
      `dist/katex/katex-fonts-extra.css ${extraCss.length} bytes`
  )
  /* eslint-enable no-console */
}

const build = async () => {
  await assertKatexVersion()
  const css = await readFile(resolve(KATEX_DIST, 'katex.min.css'), 'utf8')
  const blocks = splitFontFaces(css)
  const stats = { dropped: 0, extra: 0, inlined: 0, minimal: 0 }
  const { extraCss, minimalCss } = await distributeBlocks(blocks, stats)
  await mkdir(OUT_DIR, { recursive: true })
  await Promise.all([
    writeFile(resolve(OUT_DIR, 'katex.css'), minimalCss),
    writeFile(resolve(OUT_DIR, 'katex-fonts-extra.css'), extraCss),
  ])
  reportStats(stats, minimalCss, extraCss)
}

await build()
